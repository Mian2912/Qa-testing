public class Ejercicio_1 {
    public static void main(String[] args) {
        /**
         * 1) Declara dos variables numéricas (con el valor que desees), muestra por consola la suma, resta,
            multiplicación, división y módulo (resto de la división).
         */
        //declaro las variables
        int numero1 = 30;
        int numero2 = 20;
        
        System.out.println("Suma:\nLa Suma de " + numero1 + " y el " + numero2 + " es: " + (numero1 - numero2));
        System.out.println("\nResta:\nLa Resta de " + numero1 + " y el " + numero2 + " es: " + (numero1 - numero2));
        System.out.println("\nMultiplicacion:\nLa Multiplicacion de " + numero1 + " y el " + numero2 + " es: " + (numero1 * numero2));
        System.out.println("\nDivision:\nLa Division de " + numero1 + " y el " + numero2 + " es: " + (numero1 / numero2));
        System.out.println("\nModulo:\nLa Modulo de " + numero1 + " y el " + numero2 + " es: " + (numero1 % numero2));
        
    }
}
